					           "email"=>$_POST["editarEmail"],
					           "tel"=>$_POST["editarTelefono"],
					           "dir"=>$_POST["editarDir"],
					           "bird"=>$_POST["editarBird"]);

			   	$respuesta = ModeloClientes::mdlEditarCliente($tabla, $datos);

			   	if($respuesta == "ok"){

					echo'<script>

