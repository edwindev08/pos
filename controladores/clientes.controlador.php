			   preg_match('/^[#\.\-a-zA-Z0-9 ]+$/', $_POST["editarDir"])){

			   	$tabla = "clientes";

			   	$datos = array("id"=>$_POST["idCliente"],
			   				   "nombre"=>$_POST["editarCliente"],
